import Foundation

class ColorExtensions {}

extension UIColor {
    
    class var lavoLightBlue: UIColor {
        return UIColor(red: 172/255, green: 233/255, blue: 255/255, alpha: 1)
    }

    class var lavoBlue: UIColor {
        return UIColor(red: 0, green: 169/255, blue: 203/255, alpha: 1)
    }
    
    class var lavoSlightlyDarkBlue: UIColor {
        return UIColor(red: 72/255, green: 160/255, blue: 194/255, alpha: 1)
    }
    
    class var lavoDarkBlue: UIColor {
        return UIColor(red: 37/255, green: 54/255, blue: 76/255, alpha: 1)
    }
    
    class var lavoLightGray: UIColor {
        return UIColor(red: 234/255, green: 237/255, blue: 239/255, alpha: 1)
    }
    
    class var lavoMediumGray: UIColor {
        return UIColor(red: 216/255, green: 216/255, blue: 216/255, alpha: 1)
    }
    
    class var lavoDarkGray: UIColor {
        return UIColor(red: 49/255, green: 49/255, blue: 49/255, alpha: 1)
    }
    
    class var darkRed: UIColor {
        return UIColor(red: 181/255, green: 36/255, blue: 36/255, alpha: 1)
    }
    
}
